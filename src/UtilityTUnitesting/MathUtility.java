package UtilityTUnitesting;

public class MathUtility {
    
    /**
     * Adds two nums 
     * @param intNumber1 first int
     * @param intNumber2 second int
     * @return the results
     */
    public static int add(int intNumber1, int intNumber2) {
        return intNumber1 + intNumber2;
    }

    
    /**
     * subtracts two integers
     * @param intNumber1 the first int
     * @param intNumber2 second int
     * @return the results
     */

    public static int subtraction(int intNumber1, int intNumber2) {
        return intNumber1 - intNumber2;
    }


}
